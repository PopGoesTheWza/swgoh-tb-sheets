if (Number.parseInt === undefined)
  Number.parseInt = parseInt;

Number.parseRoman = Number.parseRoman || (function() {
  var map = { "I": 1, "V": 5, "X": 10, "L": 50, "C": 100, "D": 500, "M": 1000 };
  var char_to_int = function(char) {
    try {
      return map[char];
    } catch (e) {
      return Number.NaN;
    }
  };

  return function(str) {
    if (str === undefined) {
      return Number.NaN;
    }
    
    try {
      var pre = null;
      var value = str
                 .toUpperCase()
                 .split("")
                 .reduce(function(a, e) {
                   var curr = char_to_int(e);
                   var r = curr;
                   if(a > 0 && curr > pre) {
                     r = curr - pre * 2;
                   }
                   pre = curr;
                   return a + r;
                 }, 0);

      return value;
    } catch (e) {
      return Number.NaN;
    }
  }
})();


Number.prototype.toRoman = Number.prototype.toRoman || (function() {
  var decimal = [1000, 900, 500, 400, 100, 90, 50, 40, 10, 9, 5, 4, 1];
  var roman = { 1: "I", 4: "IV", 5: "V", 9: "IX", 10: "X", 40: "XL", 50: "L", 90: "XC", 100: "C", 400: "CD", 500: "D", 900: "CM", 1000: "M" };

  return function() {
    var int = this;
    var str = "";

    decimal
    .forEach(function(e) {
      while (int % e < int) {
        str += roman[e];
        int -= e;
      }
    });

    return str;
  }
})();

// From https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Object/keys
if (!Object.keys) {
  Object.keys = (function() {
    'use strict';
    var hasOwnProperty = Object.prototype.hasOwnProperty,
        hasDontEnumBug = !({ toString: null }).propertyIsEnumerable('toString'),
        dontEnums = [
          'toString',
          'toLocaleString',
          'valueOf',
          'hasOwnProperty',
          'isPrototypeOf',
          'propertyIsEnumerable',
          'constructor'
        ],
        dontEnumsLength = dontEnums.length;

    return function(obj) {
      if (typeof obj !== 'function' && (typeof obj !== 'object' || obj === null)) {
        throw new TypeError('Object.keys called on non-object');
      }

      var result = [], prop, i;

      for (prop in obj) {
        if (hasOwnProperty.call(obj, prop)) {
          result.push(prop);
        }
      }

      if (hasDontEnumBug) {
        for (i = 0; i < dontEnumsLength; i++) {
          if (hasOwnProperty.call(obj, dontEnums[i])) {
            result.push(dontEnums[i]);
          }
        }
      }
      return result;
    };
  }());
}
